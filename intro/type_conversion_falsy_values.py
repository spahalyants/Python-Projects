x = input("x: ")    # user types "5" → x is "5" (string)
y = int(x) + 1      # int("5") → 5, then 5 + 1 = 6
print(f"x: {x}, y: {y}")  # x: 5, y: 6


# In Python, falsy values evaluate to False in a boolean context:

# "" — empty string
# 0 — zero
# None — absence of value

# Also falsy (not shown): False, [] (empty list), {} (empty dict), 0.0.

